"""
Aggregate result JSONs produced by the other experiment scripts into
publication-ready Markdown tables.

Input: a list of JSON files (each a list of result dicts with at minimum
       ``name``, ``best_val_bpc`` and optionally ``seed``, ``variant``,
       ``lang``, ``run``).

Output: Markdown tables (grouped by lang/run) with mean ± std BPC and
        a simple Welch's t-test on CST vs matched SPM baselines where
        seeds are available.

Usage:
    python training/experiments/aggregate_results.py \\
        results_english.json results_arabic.json \\
        results_multiseed.json \\
        results_label_shuffle.json \\
        --out docs/results_tables.md
"""

from __future__ import annotations

import argparse
import json
import math
from collections import defaultdict
from pathlib import Path


def load(paths: list[str]) -> list[dict]:
    results: list[dict] = []
    for p in paths:
        with open(p) as f:
            data = json.load(f)
        if isinstance(data, dict):
            data = [data]
        for r in data:
            r["_source"] = Path(p).name
            results.append(r)
    return results


def agg(bpcs: list[float]) -> tuple[float, float, int]:
    n = len(bpcs)
    mean = sum(bpcs) / n
    var = sum((x - mean) ** 2 for x in bpcs) / max(n - 1, 1)
    return mean, var ** 0.5, n


def welch_t(a: list[float], b: list[float]) -> tuple[float, float] | None:
    """Returns (t, dof). Returns None if either group has < 2 samples."""
    if len(a) < 2 or len(b) < 2:
        return None
    ma, sa, na = agg(a)
    mb, sb, nb = agg(b)
    va, vb = sa ** 2, sb ** 2
    denom = math.sqrt(va / na + vb / nb) or 1e-12
    t = (ma - mb) / denom
    num = (va / na + vb / nb) ** 2
    den = (va / na) ** 2 / (na - 1) + (vb / nb) ** 2 / (nb - 1)
    dof = num / den if den > 0 else float("nan")
    return t, dof


def render_table(lines: list[str], title: str, rows: list[tuple[str, list[float]]]) -> None:
    lines.append(f"### {title}\n")
    lines.append("| Tokenizer | BPC (mean ± std) | N |")
    lines.append("|---|---|---|")
    for name, bpcs in rows:
        mean, std, n = agg(bpcs)
        lines.append(f"| `{name}` | {mean:.4f} ± {std:.4f} | {n} |")
    lines.append("")


def main() -> int:
    p = argparse.ArgumentParser()
    p.add_argument("inputs", nargs="+")
    p.add_argument("--out", default="-", help="Output markdown path (default stdout).")
    args = p.parse_args()

    results = load(args.inputs)
    if not results:
        print("No results loaded.")
        return 1

    # Group by (lang, run, variant-or-name)
    groups: dict[tuple[str, str], dict[str, list[float]]] = defaultdict(lambda: defaultdict(list))
    for r in results:
        lang = r.get("lang", "?")
        run = r.get("run", r.get("variant", "main"))
        name = r["name"]
        groups[(lang, run)][name].append(r["best_val_bpc"])

    lines: list[str] = [
        "# Aggregated experiment results",
        "",
        "Auto-generated by `training/experiments/aggregate_results.py`.",
        "",
    ]

    for (lang, run), rows in sorted(groups.items()):
        title = f"{lang.upper()} / {run}"
        render_table(lines, title, sorted(rows.items()))

        # Attempt pairwise significance: CST vs SPM with the same run suffix.
        cst_names = [n for n in rows if "CST" in n.upper() and "SHUF" not in n.upper()]
        spm_names = [n for n in rows if "SPM" in n.upper()]
        for c in cst_names:
            for s in spm_names:
                test = welch_t(rows[c], rows[s])
                if test is None:
                    continue
                t, dof = test
                lines.append(f"- Welch's t-test `{c}` vs `{s}`: t = {t:.3f}, dof ≈ {dof:.1f}")
        lines.append("")

    out = "\n".join(lines)
    if args.out == "-":
        print(out)
    else:
        Path(args.out).parent.mkdir(parents=True, exist_ok=True)
        Path(args.out).write_text(out)
        print(f"  wrote {args.out}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
